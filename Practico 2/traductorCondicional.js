let palabra = 'mesa';

switch (palabra) {
    case 'perro':
        console.log('Dog');
        break;
    
    case 'gato':
        console.log('Cat');
        break;
    
    case 'ventana':
        console.log('Window');
        break;

    case 'mesa':
        console.log('table');
        break;

    default:
        console.log('La palabra ingresada es incorrecta');
        break;
}